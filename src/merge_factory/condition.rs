//! 合并条件规则定义模块
//!
//! 本模块定义了三种不同的单元格合并策略，用于指导 MergeFactory 如何在 Excel 中执行合并操作。
//! 所有规则都通过 `type` 字段进行区分，并使用 snake_case 命名规范。
use serde::Deserialize;

/// 合并条件枚举
///
/// 定义了三种不同的合并策略，每种策略适用于不同的业务场景：
///
/// # 策略说明
///
/// ## Fixed（静态坐标合并）
/// 直接指定物理坐标进行合并，适用于固定位置的标题、签名栏等
///
/// ## VerticalMatch（纵向自动合并）
/// 基于列值的纵向连续性进行合并，适用于部门、分类等层级数据
///
/// ## HorizontalMatch（横向自动合并）
/// 基于行内相邻列值的一致性进行合并，适用于计划vs实际等对比数据
///
/// # 序列化说明
/// 使用 `#[serde(tag = "type")]` 实现标签化联合（Tagged Union），
/// JSON 输入必须包含 `"type"` 字段来指定具体的规则类型。
#[derive(Deserialize, Debug)]
#[serde(tag = "type", rename_all = "snake_case")]
pub enum MergeCondition {

    /// 静态坐标合并规则
    ///
    /// 通过直接指定物理坐标来定义合并区域，适用于位置固定的合并需求。
    Fixed {
        /// 物理目标区间列表
        ///
        /// 每个区间由四个 u32 值组成：[起始行, 起始列, 结束行, 结束列]
        /// 系统会自动处理坐标标准化（确保起始 <= 结束）。
        targets: Vec<[u32; 4]>,
    },

    /// 纵向自动合并规则
    ///
    /// 当指定列的连续多行内容相同时自动创建合并区域。
    /// 支持父子级联机制：多个目标列之间存在依赖关系。
    VerticalMatch {
        /// 需要扫描比对的目标列名列表
        ///
        /// 列名必须存在于输入的 DataFrame 中。
        /// 多个列名按顺序形成父子级联关系。
        targets: Vec<String>,
        /// 判定逻辑控制参数
        ///
        /// 预留扩展字段，用于控制合并的具体行为。
        /// 当 JSON 中未提供此字段时，默认为空字符串。
        #[serde(default)]
        criteria: String,
    },

    /// 横向自动合并规则
    ///
    /// 当同一行内连续列的内容相同时自动创建水平合并区域。
    /// 主要用于同类型数据的对比显示场景。
    ///
    /// # 合并逻辑
    /// 系统会扫描每一行，寻找连续相同值的列段：
    /// - 对于 ["A", "B", "C", "D"] 四列
    /// - 如果某行值为 ["值1", "值1", "值2", "值2"]
    /// - 将产生两个合并区域：A-B列 和 C-D列
    ///
    /// # 应用场景
    /// - 计划值 vs 实际值的对比合并
    /// - 多期数据的趋势合并
    /// - 相同分类的数据区域合并
    ///
    /// # 示例 JSON
    /// ```json
    /// {
    ///   "type": "horizontal_match",
    ///   "targets": ["Q1计划", "Q1实际", "Q2计划", "Q2实际"],
    ///   "criteria": "ignore_empty"
    /// }
    /// ```
    HorizontalMatch {
        /// 需要进行连续相同值检测的目标列名列表
        ///
        /// 系统会对列表中的列按顺序进行扫描，寻找连续相同值的段落。
        /// 合并区域将覆盖从第一个到最后一个相同值的所有列。
        ///
        /// # 处理方式
        /// - 按列表顺序扫描
        /// - 寻找连续相同值的区间
        /// - 为每个区间创建对应的合并区域
        targets: Vec<String>,
        /// 判定逻辑控制参数
        ///
        /// 预留扩展字段，用于控制合并的具体行为。
        /// 当前支持的值：
        /// - "" (默认): 标准合并逻辑
        ///
        /// # 默认值
        /// 当 JSON 中未提供此字段时，默认为空字符串。
        #[serde(default)]
        criteria: String,
    },

}