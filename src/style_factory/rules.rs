use serde::Deserialize;
use crate::style_factory::StyleCondition;

/// 局部樣式覆蓋規則
///
/// 提供細粒度的樣式控制，在已選中行的內部針對特定單元格進行樣式替換。
///
/// # 設計理念
///
/// ## 條件驅動
/// 通過 `col_conditions` 定義精確的匹配條件，實現靈活的單元格選擇。
///
/// ## 覆蓋優先級
/// 覆蓋樣式具有最高優先級，會替代行級基礎樣式。
///
/// # 使用場景
/// - 條件高亮：特定值的單元格特殊顯示
/// - 分類著色：不同類型數據的差異化呈現
/// - 狀態標識：異常數據的視覺提醒
#[derive(Deserialize, Debug)]
pub struct StyleOverride {

    /// 覆蓋樣式名稱
    ///
    /// 當列條件滿足時應用的樣式，會覆蓋所在行的基礎樣式。
    /// 該樣式必須在 Workbook 的樣式池中預先定義。
    pub style: String,

    /// 列條件集合
    ///
    /// 定義哪些單元格應該應用此覆蓋樣式。支持多種條件類型：
    /// - `Match`: 指定特定列名
    /// - `ValueRange`: 基於數值範圍的條件
    /// - `Find`: 字符串內容匹配
    /// - `Equal`: 列間一致性檢查
    ///
    /// # 邏輯關係
    /// 多個條件之間為邏輯「與」（AND）關係。
    pub col_conditions: Vec<StyleCondition>,

}

/// 樣式應用策略
///
/// 定義命中行規則後的兩層樣式應用機制。
///
/// # 兩層渲染模型
///
/// ## 第一層：行級基礎樣式
/// 為整行應用統一樣式，提供默認視覺效果。
///
/// ## 第二層：單元格級覆蓋
/// 在基礎樣式之上，針對特定單元格進行精確的樣式調整。
///
/// # 設計優勢
/// - **性能優化**：減少重複的樣式定義
/// - **維護簡便**：統一的行級樣式管理
/// - **靈活性強**：支持細粒度的局部調整
#[derive(Deserialize, Debug)]
pub struct ApplyAction {

    /// 行級基礎樣式
    ///
    /// 命中行的所有單元格首先應用此樣式。
    /// 必須在 Workbook 的樣式池中預先定義。
    pub style: String,

    /// 單元格級覆蓋規則
    ///
    /// 可選的局部樣式覆蓋，用於精確控制特定單元格的顯示效果。
    ///
    /// # 覆蓋示例
    /// ```text
    /// 基礎樣式：整行灰色背景
    /// 覆蓋規則：金額列負數顯示紅色字體
    /// 結果：行背景灰色，負金額單元格紅字
    /// ```
    pub overrides: Option<Vec<StyleOverride>>,

}

/// 樣式規則定義
///
/// 描述在特定數據條件下如何應用樣式的完整規則。
///
/// # 核心概念
///
/// ## 條件匹配
/// 通過 `row_conditions` 定義行選擇條件。
///
/// ## 樣式應用
/// 通過 `apply` 定義匹配後的樣式處理策略。
///
/// # 執行流程
/// ```text
/// 1. 遍歷 DataFrame 的每一行
/// 2. 檢查是否滿足所有行條件
/// 3. 若滿足，應用對應的樣式策略
/// 4. 繼續處理下一條規則（可能覆蓋）
/// ```
#[derive(Deserialize, Debug)]
pub struct StyleRule {

    /// 行選擇條件
    ///
    /// 定義哪些行應該應用此樣式規則。多個條件之間為邏輯「與」關係。
    /// 只有當一行同時滿足所有條件時才會被選中。
    ///
    /// # 條件類型
    /// - `Index`: 基於行號的物理定位
    /// - `ValueRange`: 基於數值範圍的條件
    /// - `Match`: 基於集合成員的匹配
    /// - `Find`: 基於字符串內容的查找
    /// - `Equal`: 基於列間一致性的檢查
    pub row_conditions: Vec<StyleCondition>,

    /// 樣式應用策略
    ///
    /// 定義選中行後的樣式處理方式，包括基礎樣式和可選的覆蓋規則。
    pub apply: ApplyAction,

}